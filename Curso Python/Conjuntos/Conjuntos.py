#Uma coleção não ordenada que não permite elementos repetidos
colecao = {2, 2, 2, "python", "python"}
print(colecao)

#Transformar uma lista em um conjunto
lista = [1, 1, 1, 1, 2, 2, 3]
conjunto = set(lista)
print(conjunto)

#Adcionando elementos
colecao.add(3)
print(colecao)

#Removendo elementos
colecao.remove(3)
print(colecao)