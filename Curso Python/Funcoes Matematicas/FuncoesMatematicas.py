import math

#Retorna o maior e menor inteiro em relação ao número que passar
print(math.ceil(3.14))

#Retorna o valor absoluto
print(math.fabs(-5))

#Retorna o fatorial do número que passar no parametro
print(math.factorial(5))

#Retonar o menor ou igual ao número que passar no parametro
print(math.floor(3.14))

#Retorna o exponencial neperiano
print(math.exp(2))

#Retorna o logaritmo
print(math.log(10))

#Faz a exponenciação, primeiro parametro é base e o segundo é o expoente
print(math.pow(2, 3))

#Retorna a raiz quadrada do numero passado por parametro
print(math.sqrt(25))